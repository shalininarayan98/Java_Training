package assignments_iteration_control;

public class problemOne {
	
	public static void main (String [] args) {
		
		int input = 10;
		int factorial = 1;
		
		for(int i = 1; i <= input; i++) {
			
			factorial = factorial * i;
			System.out.println("The factorial for " + i+ " is: " + factorial);
		
		
	}

}

	
}
